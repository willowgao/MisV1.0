package bean;

public class Merchandise {

	private String merchandiseId;
	private String merchandiseType;
	private String merchandiseName;
	private String merchandiseTandard;
	private String merchandiseBrand;
	private String merchandisePrice;

	public String getMerchandisePrice() {
		return merchandisePrice;
	}

	public void setMerchandisePrice(String merchandisePrice) {
		this.merchandisePrice = merchandisePrice;
	}

	public String getMerchandiseId() {
		return merchandiseId;
	}

	public void setMerchandiseId(String merchandiseId) {
		this.merchandiseId = merchandiseId;
	}

	public String getMerchandiseType() {
		return merchandiseType;
	}

	public void setMerchandiseType(String merchandiseType) {
		this.merchandiseType = merchandiseType;
	}

	public String getMerchandiseName() {
		return merchandiseName;
	}

	public void setMerchandiseName(String merchandiseName) {
		this.merchandiseName = merchandiseName;
	}

	public String getMerchandiseTandard() {
		return merchandiseTandard;
	}

	public void setMerchandiseTandard(String merchandiseTandard) {
		this.merchandiseTandard = merchandiseTandard;
	}

	public String getMerchandiseBrand() {
		return merchandiseBrand;
	}

	public void setMerchandiseBrand(String merchandiseBrand) {
		this.merchandiseBrand = merchandiseBrand;
	}

}
